export type CourseLesson = {
  order: number;
  title: string;
  sourceUrl: string;
  videoId?: string;
  embedUrl?: string;
  cs50Url?: string;
};

const embed = (videoId: string) => `https://www.youtube.com/embed/${videoId}?enablejsapi=1&rel=0`;
const source = (videoId: string) => `https://youtu.be/${videoId}`;
const videoLesson = (order: number, title: string, videoId: string, sourceUrl = source(videoId), extra?: { cs50Url?: string }): CourseLesson => ({
  order,
  title,
  videoId,
  embedUrl: embed(videoId),
  sourceUrl,
  ...extra,
});
const linkLesson = (order: number, title: string, sourceUrl: string): CourseLesson => ({ order, title, sourceUrl });
const linkLessonsFromTitles = (titles: string[], sourceUrl: string): CourseLesson[] =>
  titles.map((title, index) => linkLesson(index + 1, title, sourceUrl));

